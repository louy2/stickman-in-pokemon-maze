// 绘制像素精灵的函数
function drawSprite(ctx, spriteData, x, y, size) {
    if (!spriteData) return;

    const pixels = spriteData.pixels;
    const palette = spriteData.palette;
    const pixelSize = size / 16;

    for (let py = 0; py < 16; py++) {
        for (let px = 0; px < 16; px++) {
            const colorIndex = pixels[py][px];
            if (colorIndex === 0) continue; // 透明

            ctx.fillStyle = palette[colorIndex];
            ctx.fillRect(
                x + px * pixelSize,
                y + py * pixelSize,
                pixelSize + 0.5,
                pixelSize + 0.5
            );
        }
    }
}

// 创建精灵缓存（提高性能）
const spriteCache = {};

function getCachedSprite(id, size) {
    const key = `${id}_${size}`;
    if (spriteCache[key]) return spriteCache[key];

    const spriteData = POKEMON_SPRITES[id];
    if (!spriteData) return null;

    const canvas = document.createElement('canvas');
    canvas.width = size;
    canvas.height = size;
    const ctx = canvas.getContext('2d');

    drawSprite(ctx, spriteData, 0, 0, size);

    spriteCache[key] = canvas;
    return canvas;
}

